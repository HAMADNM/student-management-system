database_url = config("DATABASE_URL", default="")

if database_url.startswith(("postgres://", "postgresql://")):
    db = urlparse(database_url)
    DATABASES = {
        "default": {
            "ENGINE": "django.db.backends.postgresql",
            "NAME": db.path.lstrip("/"),
            "USER": db.username,
            "PASSWORD": db.password,
            "HOST": db.hostname,
            "PORT": db.port or 5432,
        }
    }
elif database_url.startswith(("sqlite:///", "sqlite://")):
    db = urlparse(database_url)
    sqlite_path = db.path.lstrip("/")
    DATABASES = {
        "default": {
            "ENGINE": "django.db.backends.sqlite3",
            "NAME": BASE_DIR / sqlite_path if sqlite_path and sqlite_path != ":memory:" else sqlite_path or BASE_DIR / "db.sqlite3",
        }
    }
else:
    DATABASES = {
        "default": {
            "ENGINE": "django.db.backends.sqlite3",
            "NAME": BASE_DIR / "db.sqlite3",
        }
    }

